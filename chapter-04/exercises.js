// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step = start < end ?  1 : -1) {
  
  let output = []
  
  if(start === end){
    return []

  }

  if (step > 0) {
    for (var i = start; i <= end; i += step) {
      output.push(i);
    }
    
  } else if (step < 0) {
    for (var i = start; i >= end; i += step) {
      output.push(i);
    }
  }
  return output;

}

////////////////////////////////////////////////////////////////////////////////
// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(array) {
  let add = 0
  for(var i = 0; i < array.length; i++){
    add += array[i]
  }
  return add;

}

