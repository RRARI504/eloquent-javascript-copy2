////////////////////////////////////////////////////////////////////////////////
// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step) {
  let output = []

  

  if (step > 0) {
    for (var i = start; i <= end; i += step) {
      output.push(i);
    }
  } else if (step < 0) {
    for (var i = start; i >= end; i += step) {
      output.push(i);
    }
  }
  return output;

}

