// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  if(num1 < num2){
    return Math.min(num1)
  }else{
    return Math.min(num2)
  }

}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {
  //base
  if(n < 0){
    return false;
  }

  //recursion
  if(n === 0){
    return true;
  }

  if(n === 1){
    return false;
  }else{
    return isEven(n - 2)
  }

}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  let count = 0;
  for(var i = 0; i < string.length; i++){
    if(string[i] === char){
      count += 1

    }


  }
  return count;

}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  //returns a number that indicates how many uppercase B 
  //characters there are in the string.
  let count = 0;
  for(var i = 0; i < string.length; i++){
   if(string[i] === 'B'){
    count += 1;
   }

  }
  return count;

 
}

